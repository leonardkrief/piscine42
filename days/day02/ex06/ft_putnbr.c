int		ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int a;
	int b;

	a = nb;
	b = a % 10;
	if (nb < 0)
	{
		a = -nb;
		ft_putchar('-');
	}
	while (a / 10 != 0)
	{
		a /= 10;
		b = 10 * b + a % 10;
	}
	while (b / 10 != 0)
	{
		ft_putchar(b % 10 + '0');
		b /= 10;
	}
	ft_putchar(b + '0');
	ft_putchar('\n');
}
