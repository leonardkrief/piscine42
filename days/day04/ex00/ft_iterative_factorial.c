int		ft_iterative_factorial(int nb)
{
	int	n;

	n = 1;
	if ((nb < 0) || (nb > 12))
	{
		return (0);
	}
	else
	{
		while (nb >= 1)
		{
			n *= nb;
			nb--;
		}
		return (n);
	}
}
