int	ft_is_prime(int nb)
{
	int i;
	int z;

	i = 2;
	z = 1;
	if ((nb == 0) || (nb == 1))
	{
		return (0);
	}
	while ((i < 46341) && (i * i <= nb) && (z == 1))
	{
		if ((nb % i) == 0)
		{
			z = 0;
		}
		i++;
	}
	return (z);
}
